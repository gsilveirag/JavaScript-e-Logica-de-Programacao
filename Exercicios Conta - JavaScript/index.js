let numeroUm = window.prompt("Digite o Primeiro numero:")
let numeroDois = window.prompt ("Digite o Segundo numero:")

numeroUm = parseFloat(numeroUm)
numeroDois = parseFloat(numeroDois)

let soma = numeroUm + numeroDois
alert (`Resultado da soma:  ${soma}`)

let multiplcacao = numeroUm * numeroDois
alert (`Resultado da multiplicacão: ${multiplcacao}`)

let subtracao = numeroUm - numeroDois
alert (`Resuldado da subtracão: ${subtracao}`)

let divisao = numeroUm / numeroDois
alert (`Resultado da divisão: ${divisao}`)