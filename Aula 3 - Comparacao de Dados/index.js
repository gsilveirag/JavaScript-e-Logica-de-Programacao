var numeroExemplo = 3

console.log(numeroExemplo == 3) // compara o valor e nao o tipo
console.log(numeroExemplo == "3") // true
console.log(numeroExemplo === "3") //se o tipo for diferente vai dar false (operador mais restrito)
console.log(numeroExemplo != 3) // false
console.log(numeroExemplo !== "3") // true